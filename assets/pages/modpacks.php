<h2>Modpacks</h2>
<p class="lead">First and foremost: <b>We like modpacks.</b><br>
What we would like is a link to our website on your modlist, and a tweet so we can check your pack out when it's released.</p>
<p>You can't remove or alter the LICENCE file or other form of credits from any form of distribution.<br>
If you use a custom jar you must make that clear. A simple name change (to for example "PlaceableTools-unofficial.jar") suffices.<br></p>
<p>The full licence file can be found on every gihub repository.</p>
