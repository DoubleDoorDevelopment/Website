<h2>Modpacks</h2>
<p class="lead">First and foremost: <b>We like modpacks.</b><br>
What we would like is a link to our website on your modlist, and a tweet so we can check your pack out when it's released.</p>
<p>Of course you must still comply with our licence:<br>
You can't remove the LICENCE file (or other form of credits) from any form of distribution.<br>
If you use a custom jar (or modify one) you must make that clear. (So we don't get bugreports because of your changes.) A simple name change (to for example "PlaceableTools-unofficial.jar") suffices.<br>
<b>The full licence file can be found on every gihub repository.</b></p>
<p><b>If you are not sure or have questions, ask us!</b></p>
